package com.rehab.model.type;

/**
 * Enumeration which stores time units.
 * They are used in {@link com.rehab.model.Pattern} and {@link com.rehab.model.Period}.
 */
public enum TimeUnit {

    /**
     * Time unit that is used for single day.
     */
    DAY,

    /**
     * Time unit that is used for one week.
     */
    WEEK,

    /**
     * Time unit that is used for one month.
     */
    MONTH
}
